package HeuristicaConstrutivaInicial;


public class StringResultado {
    
    private String s;
    private String a;
    private Integer i;
    private Double fav;

    public StringResultado (){
        this.s ="";
        this.a ="";}

    /**
     * @return the s
     */
    public String getS() {
        return s;
    }

    /**
     * @param s the s to set
     */
    public void setS(String s) {
        this.s = s;
    }

    /**
     * @return the a
     */
    public String getA() {
        return a;
    }

    /**
     * @param a the a to set
     */
    public void setA(String a) {
        this.a = a;
    }

    /**
     * @return the i
     */
    public Integer getI() {
        return i;
    }

    /**
     * @param i the i to set
     */
    public void setI(Integer i) {
        this.i = i;
    }

    /**
     * @return the fav
     */
    public Double getFav() {
        return fav;
    }

    /**
     * @param fav the fav to set
     */
    public void setFav(Double fav) {
        this.fav = fav;
    }

}