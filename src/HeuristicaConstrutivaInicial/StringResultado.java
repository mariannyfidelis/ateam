package HeuristicaConstrutivaInicial;


public class StringResultado {
    
    private String s;
    private String a;
    private Integer i;
    private Double fav;

    public StringResultado (){
        this.s ="";
        this.a ="";}

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public Integer getI() {
        return i;
    }

    public void setI(Integer i) {
        this.i = i;
    }

    public Double getFav() {
        return fav;
    }

    public void setFav(Double fav) {
        this.fav = fav;
    }

}